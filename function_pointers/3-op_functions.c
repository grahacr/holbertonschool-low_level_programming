#include "3-calc.h"
#include <stdlib.h>
#include <stdio.h>
/**
 * op_add - add integers
 * @a: 1st integer input
 * @b: 2nd integer input
 * Return: solution based on operator
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtract integers
 * @a: 1st integer input
 * @b: 2nd integer input
 * Return: solution based on operator
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply integers
 * @a: 1st integer input
 * @b: 2nd integer input
 * Return: solution based on operator
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide integers
 * @a: 1st integer input
 * @b: 2nd integer input
 * Return: solution based on operator
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulo with integers
 * @a: 1st integer input
 * @b: 2nd integer input
 * Return: solution based on operator
 */
int op_mod(int a, int b)
{
	return (a % b);
}
